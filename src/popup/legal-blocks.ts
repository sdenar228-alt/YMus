/**
 * Returns the legal block HTML string for a given service.
 * Falls back to a generic message with Telegram link if the service is unknown
 * or content fails to render.
 */
export function getLegalBlockHtml(service: ServiceId): string {
  try {
    const paragraphs = LEGAL_CONTENT[service];
    if (!paragraphs || paragraphs.length === 0) {
      return FALLBACK_HTML;
    }
    return wrapLegalBlock(paragraphs);
  } catch {
    return FALLBACK_HTML;
  }
}
